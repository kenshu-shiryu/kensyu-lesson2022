
public class Sample44 {

	public static void main(String[] args) {
		int a = 50;
		int b = 40;
		int c = 30;
		int d = 20;
		System.out.println("a = " + a + " b = " + b + " c = " + c + " d = " + d);
		System.out.println("a = " + a++);
		System.out.println("a = " + a + " b = " + b + " c = " + c + " d = " + d);
		System.out.println("b = " + ++b);
		System.out.println("a = " + a + " b = " + b + " c = " + c + " d = " + d);
		System.out.println("b = " + c--);
		System.out.println("a = " + a + " b = " + b + " c = " + c + " d = " + d);
		System.out.println("b = " + --d);
		System.out.println("a = " + a + " b = " + b + " c = " + c + " d = " + d);
		

	}

}
